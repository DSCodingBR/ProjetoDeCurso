package Model;

public class Filial {
	
	private String nome;
	private LocalidadeFilial localidadefilial;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public LocalidadeFilial getLocalidadefilial() {
		return localidadefilial;
	}
	public void setLocalidadefilial(LocalidadeFilial localidadefilial) {
		this.localidadefilial = localidadefilial;
	}
	
}
